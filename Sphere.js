// class defining a sphere
export class Sphere {
  // Given x, y, z coordinates, radius, and the color values r, g, b, construct
  // a sphere
  constructor(x, y, z, radius, r, g, b) {
    this.x = x;
    this.y = y;
    this.z = z;
    this.radius = radius;
    this.r = r;
    this.g = g;
    this.b = b;
  }
}
